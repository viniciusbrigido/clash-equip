// Função para obter dados do equipamento
export const getEquipmentData = (equipmentName) => {
  return EQUIPMENT_DATA[equipmentName] || {
    name: equipmentName,
    hero: 'Unknown',
    isEpic: false,
    maxLevel: 18,
    image: '/equipment-icons/default-equipment.png'
  };
};

// Função para obter equipamentos por herói
export const getEquipmentsByHero = (heroName) => {
  return Object.values(EQUIPMENT_DATA).filter(equipment => equipment.hero === heroName);
};

// Função para verificar se está no nível máximo
export const isMaxLevel = (equipmentName, level) => {
  const equipment = getEquipmentData(equipmentName);
  return level >= equipment.maxLevel;
};
